/**
 * Renders a real brand logo for known platforms (facebook, tiktok, shopee,
 * lazada, line, youtube, instagram). Falls back to a Material Symbol for
 * anything else (e.g. website, other). Size follows the font-size set via
 * `className` (the svg is 1em square), matching how <Icon> sizes.
 */
export function BrandIcon({
  name,
  fallback,
  className,
}: {
  name: string;
  fallback?: string;
  className?: string;
}) {
  const glyph = BRAND_ICONS[name];
  if (!glyph) {
    return fallback ? <Icon name={fallback} className={className} /> : null;
  }
  return (
    <svg
      aria-hidden
      viewBox={glyph.viewBox}
      className={cn("inline-block h-[1em] w-[1em]", className)}
    >
      {glyph.node}
    </svg>
  );
}
